package day2;

public class stringexmple3 {

    public static void main(String[] args) {

        String name = "Abhishek is from chitradurga";

        System.out.println(name.split(" ")[0]);
        System.out.println(name.split(" ")[1]);
        System.out.println(name.split(" ")[2]);
        System.out.println(name.split(" ")[3]);


    }


    public static class stringexample1 {
        public static void main(String[] args) {

            String name = "Abhishek";
            System.out.println(name.toUpperCase());
            System.out.println(name.toLowerCase());
            System.out.println(name.length());
            System.out.println(name.startsWith("Abh"));
            System.out.println(name.endsWith("hek"));
            System.out.println(name.contains("is"));

        }
    }
}












